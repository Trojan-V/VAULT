package me.vault.vaultgame.utility;


/**
 * This interface provides various constants that are used within many different classes of this program,
 * because literals should be avoided within the code directly.
 *
 * @author Vincent Wolf
 */
public interface Constants {
    /**
     * A constant to represent an empty string.
     */
    String EMPTY_STRING = "";


    /**
     * A constant to represent a whitespace character.
     */
    char WHITESPACE = ' ';


    /**
     * A constant to represent a dash character.
     */
    char DASH = '-';
}
